def classify(exc: BaseException) -> ErrorCode:
    """Подобрать код для исключения. Никогда не бросает, всегда возвращает код."""
    for klass in type(exc).__mro__:
        found = _DOMAIN_CODES.get(klass)
        if found is not None:
            return found
    # Правила снаружи, MRO внутри: специфичное правило (httpx.TimeoutException)
    # обязано выиграть у catch-all того же модуля даже для подкласса (ReadTimeout).
    for prefix, expected_name, code in _EXTERNAL_CODES:
        for klass in type(exc).__mro__:
            module = getattr(klass, "__module__", "") or ""
            if module.startswith(prefix) and (not expected_name or klass.__name__ == expected_name):
                return code
    if isinstance(exc, TimeoutError):
        return _SYSTEM[5]  # 1402 (включая asyncio.TimeoutError)
    if isinstance(exc, ConnectionError):
        return _SYSTEM[4]  # 1401
    return UNCLASSIFIED


def format_error_id(exc: BaseException, fingerprint8: str) -> str:
    """``E<код>-<fp8>`` — то, что видит юзер и что ищется на дашборде."""
    return f"E{classify(exc).code}-{fingerprint8}"


def all_codes() -> list[ErrorCode]:
    """Полный каталог для генерации справочника (уникальные, по возрастанию кода)."""
    seen: dict[int, ErrorCode] = {}
    for code in (
        *_SYSTEM,
        *_DOMAIN_CODES.values(),
        *(c for _, _, c in _EXTERNAL_CODES),
        UNCLASSIFIED,
    ):
        seen.setdefault(code.code, code)
    return [seen[k] for k in sorted(seen)]
